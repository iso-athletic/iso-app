<template>
  <v-app dark>
    <v-toolbar>
      <v-toolbar-title>Iso Athletic</v-toolbar-title>
      <v-spacer></v-spacer>
      <v-toolbar-items class="hidden-xs-and-down">
        <v-btn :to="'/'"
                      v-if="authenticated"
                      flat>
                      Dashboard
        </v-btn>
        <v-btn flat
               v-if="!authenticated"
               @click="login()">
               Log In
        </v-btn>
        <v-btn flat
               v-if="!authenticated"
               @click="login()">
               Sign Up
        </v-btn>
        <v-btn flat :to="{ path: '/settings', params: {} }"
                     v-if="authenticated">
                     Settings
        </v-btn>
        <v-btn flat
               v-if="authenticated"
               @click="logout()">
               Log Out
        </v-btn>
      </v-toolbar-items>
    </v-toolbar>
    <v-content>
    <v-container fluid class="overall-container">
      <router-view
            :auth="auth"
            :authenticated="authenticated">
      </router-view>
    </v-container>
    </v-content>
  </v-app>
</template>

<script>
import AuthService from "./auth/AuthService";

const auth = new AuthService();
const { login, logout, authenticated, authNotifier } = auth;
export default {
  name: "app",
  data() {
    authNotifier.on("authChange", authState => {
      this.authenticated = authState.authenticated;
    });
    return {
      auth,
      authenticated
    };
  },
  methods: {
    login,
    logout
  }
};
</script>

